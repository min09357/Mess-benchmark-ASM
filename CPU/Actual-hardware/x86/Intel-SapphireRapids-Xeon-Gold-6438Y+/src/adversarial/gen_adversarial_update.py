#!/usr/bin/env python3
"""
Generate adversarial_update.c: 51 functions ADVERSARIAL_update_R (R in 0,2,4,...,100).

Each function:
  - Takes 100 accesses into target_addrs[] starting at the given pointer.
  - R% of accesses are reads (local += *addrs[i]), (100-R)% are writes (*addrs[i] = 0).
  - R/W interleave rule: is_read[slot] = ((slot * R) % 100) < R  (same as random_update.c).
  - nop(pause) called after every 5 accesses (20 nop calls total).

Signature:
  void ADVERSARIAL_update_R(volatile uint64_t **addrs, int *pause, volatile uint64_t *sink)

'addrs' points to a slice of 100 consecutive entries in the target address array.
The caller is responsible for wrapping the index before each call.
"""

import sys

ACCESSES_PER_CALL = 100
GROUP_SIZE = 5       # nop every GROUP_SIZE accesses

def is_read(slot, R):
    return ((slot * R) % 100) < R

def generate_function(R):
    lines = []
    lines.append(f"/* ADVERSARIAL_update_{R}: {R} read(s) + {100-R} write(s). */")
    lines.append(f"void ADVERSARIAL_update_{R}(volatile uint64_t **addrs,")
    lines.append(f"                              int *pause, volatile uint64_t *sink)")
    lines.append("{")
    lines.append("    volatile uint64_t local = *sink;")

    groups = ACCESSES_PER_CALL // GROUP_SIZE
    for g in range(groups):
        lines.append(f"    /* group {g+1} */")
        for s in range(GROUP_SIZE):
            slot = g * GROUP_SIZE + s
            if is_read(slot, R):
                lines.append(f"    local += *addrs[{slot}]; /* R slot {slot} */")
            else:
                lines.append(f"    *addrs[{slot}] = 0;      /* W slot {slot} */")
        lines.append("    nop(pause);")
        lines.append("")

    lines.append("    *sink = local;")
    lines.append("}")
    return "\n".join(lines)

def main():
    header = """\
/*
 * adversarial_update.c — generated by gen_adversarial_update.py
 *
 * 51 variants of ADVERSARIAL_update_R (R in {0, 2, 4, ..., 100}).
 * Each function: 100 accesses into a target-bank address slice,
 *   {R} reads and {100-R} writes, nop() called after every 5 accesses.
 * Compile with -fno-tree-vectorize -fno-tree-slp-vectorize to keep
 * 100 individual load/store instructions (no gather/scatter folding).
 */

#include "adversarial_update.h"
"""

    with open("adversarial_update.c", "w") as f:
        f.write(header)
        for R in range(0, 101, 2):
            f.write("\n")
            f.write(generate_function(R))
            f.write("\n")

    print(f"Generated adversarial_update.c with {len(range(0, 101, 2))} functions.",
          file=sys.stderr)

if __name__ == "__main__":
    main()
